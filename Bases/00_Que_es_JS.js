/*

¿Qué es JavaScript?

    JavaScript (JS) es un lenguaje de programación creado en 1995 por Brendan Eich.
    Es una de las tecnologías principales de la web junto con HTML y CSS.

    Importante: A pesar del nombre, no está relacionado con Java.
    El nombre fue una decisión de marketing.

    Características principales:

    - Se ejecuta en el navegador (lado del cliente).
    Permite crear páginas dinámicas, reaccionar a eventos
    y comunicarse con el servidor sin recargar la página.

    - Es multiparadigma.
    Puedes usar programación orientada a objetos,
    funcional o imperativa.

    - Es dinámicamente tipado.
    Una variable puede cambiar de tipo durante la ejecución.

    - Es interpretado.
    El navegador ejecuta el código directamente
    (no necesitas compilarlo antes).


¿Como incluir JS en una página HTML?

    Puedes incluir JS de dos formas:

    1. En línea (inline):
    <script>
        console.log("Hola desde JS en línea");
    </script>

    2. Externo:
    <script src="script.js"></script>
    Donde "script.js" es un archivo que contiene tu código JS.

    En ambos casos, el código dentro de la etiqueta <script> se ejecutará
    cuando el navegador lo lea.

    Nota: Es recomendable colocar las etiquetas <script> al final del cuerpo
    del documento (antes de </body>) para asegurar que el HTML se cargue
    antes de ejecutar el JS.
*/